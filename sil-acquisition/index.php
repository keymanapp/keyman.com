<h2 class="red underline">SIL acquires rights to Keyman software</h2>

<p>(July 2018) Update: This page originally referenced purchasing products through the Tavultesoft online store.
    That store is now closed and this page has been updated to reflect this.</p>
<br/>

<p>(October 2015) <a href='http://www.sil.org'>SIL International</a>, a global language development NGO and Tavultesoft, a leading supplier of keyboarding software are pleased to announce that as of 
30th September 2015, SIL International has purchased all rights to Tavultesoft’s Keyman® keyboarding software. SIL International has purchased Keyman Desktop, Keyman Developer, Keyman for Mac, 
Keyman for Android, Keyman for iPhone and iPad and KeymanWeb.</p>

<blockquote class='sidebar'>
<p><em>We’re excited to be involved in the next stage of developing input methods for the world together with SIL International. The linguistic resource and experience that SIL International brings to the table will be a great boost to Keyman's future development. We believe that Keyman users all over the world will be well served by this acquisition.</em></p>
<p>&mdash; Marc Durdin, creator of Keyman, CEO and Chief Developer for Tavultesoft</p>
</blockquote>

<p>Technical support is available on <a href='https://community.software.sil.org/c/keyman'>SIL Keyman Community</a>.</p>

<p>If you have questions about the acquisition, and how it may affect you, please read our Frequently Asked Questions section below.</p>

